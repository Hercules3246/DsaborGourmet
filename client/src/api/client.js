import { basePath, apiVersion } from "./config";

export function getClientSearch(name) {
  const url = `${basePath}/${apiVersion}/client-search?name=${name}`;

  return fetch(url)
    .then((response) => {
      return response.json();
    })
    .then((result) => {
      return result;
    })
    .catch((err) => {
      return err.message;
    });
}

export function updateClientApi(token, clientId, data) {
  const url = `${basePath}/${apiVersion}/update-client/${clientId}`;

  const params = {
    method: "PUT",
    headers: {
      "Content-Type": "application/json",
      Authorization: token,
    },
    body: JSON.stringify(data),
  };

  return fetch(url, params)
    .then((response) => {
      return response.json();
    })
    .then((result) => {
      return result.message;
    })
    .catch((err) => {
      return err;
    });
}

